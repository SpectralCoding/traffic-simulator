		private static Tile[,] m_Tiles;

		public static Size MapSizePixels {
			get {
				return new Size(m_Tiles.GetLength(1) * 25, m_Tiles.GetLength(0) * 25);
			}
		}

		/// <summary>
		/// Loads the tiles then sets up the m_Tiles array according to IntMap
		/// </summary>
		public static void BuildWorld() {
			TileManager.LoadTiles();
			m_Tiles = new Tile[40, 40];
			for (int y = 0; y < 40; y++) {
				for (int x = 0; x < 40; x++) {
					m_Tiles[x, y] = TileManager.GetTile(IntMap[x,y]);
				}
			}
		}

		/// <summary>
		/// Returns a Surface type depicting the appropriate part of the Map to display
		/// </summary>
		/// <param name="offset"> TopLeft corner of the ViewSpace</param>
		/// <param name="viewSize">Size of the space to view</param>
		/// <returns>Surface type of space viewSize</returns>
		public static Surface GetViewSpace(Point offset, Size viewSize) {
			Surface viewspace = new Surface(viewSize);

			Point startTile = new Point(offset.X / 25, offset.Y / 25);
			Point endTile = new Point((offset.X + viewSize.Width) / 25, (offset.Y + viewSize.Height) / 25);
			int offsetX = offset.X % 25;
			int offsetY = offset.Y % 25;

			for (int y = startTile.Y, yCnt = 0; y <= endTile.Y; y++, yCnt++) {
				for (int x = startTile.X, xCnt = 0; x <= endTile.X; x++, xCnt++) {
					viewspace.Blit(
						m_Tiles[y, x].Surface,
						new Point(offsetX >= 0 ? (25 - offsetX) + ((xCnt - 1) * 25) : (25 - offsetX) * (xCnt * 25), offsetY >= 0 ? (25 - offsetY) + ((yCnt - 1) * 25) : (25 - offsetY) * (yCnt * 25)),
						new Rectangle(0, 0, 25, 25));
				}
			}

			return viewspace;
		}

